# import math functions from python library.
from math import sqrt
print(sqrt(100))

# import operator functions from python library.
# Once a function is imported, it can be called multiple times.
from operator import add, sub, mul
print(add(100, 2))
print(sub(100, mul(7, add(8, 4))))

# = is an assignment symbol, can help to bind name with value.
from math import pi
area = pi * 71 / 233

# a name can also be bound to functions
function_sqrt = sqrt
print(function_sqrt(100))

